
function Captain(name,story,difficulty,funds,reputation,food){
  this.name=name;
  this.story=story;
  this.difficulty=difficulty;
  this.funds=funds;
  this.reputation=reputation;
  this.food=food;
  this.population=100;
  this.timeLine=0;
}

